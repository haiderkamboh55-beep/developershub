const API_BASE = window.location.hostname === 'localhost' || window.location.hostname === '127.0.0.1'
  ? 'http://127.0.0.1:5000/api'
  : '/api';


// --- API FETCH WRAPPERS ---

async function apiCall(endpoint, method = 'GET', body = null, useAuth = false) {
  const headers = {};
  if (body && !(body instanceof FormData)) {
    headers['Content-Type'] = 'application/json';
  }
  
  if (useAuth) {
    const token = localStorage.getItem('token');
    if (!token) throw new Error('No token found');
    headers['Authorization'] = `Bearer ${token}`;
  }

  const options = { method, headers };
  if (body) {
    options.body = body instanceof FormData ? body : JSON.stringify(body);
  }

  try {
    const response = await fetch(`${API_BASE}${endpoint}`, options);
    const data = await response.json().catch(() => ({}));
    
    if (!response.ok) {
      throw new Error(data.error || 'Something went wrong');
    }
    
    return data;
  } catch (error) {
    console.error('API Error:', error);
    throw error;
  }
}

// Public Methods
const apiGet = (endpoint) => apiCall(endpoint);
const apiPost = (endpoint, body) => apiCall(endpoint, 'POST', body);

// Admin Methods
const apiAdmin = (method, endpoint, body = null) => apiCall(endpoint, method, body, true);

